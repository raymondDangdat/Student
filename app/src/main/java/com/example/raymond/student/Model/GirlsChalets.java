package com.example.raymond.student.Model;

public class GirlsChalets {

    private String Room;
    private String Image;

    public GirlsChalets() {
    }

    public GirlsChalets(String room, String image) {
        Room = room;
        Image = image;
    }

    public String getRoom() {
        return Room;
    }

    public void setRoom(String room) {
        Room = room;
    }

    public String getImage() {
        return Image;
    }

    public void setImages(String image) {
        Image = image;
    }

}
